package dev.fragmentcode.api.mixin;

/**
 * Тип точки внутри метода, используемый в &#64;At.
 *
 * HEAD   — самое начало метода, до выполнения любой оригинальной логики.
 * TAIL   — самый конец метода, прямо перед return (оригинальная логика
 *          уже выполнена).
 * INVOKE — рядом с конкретным вызовом метода внутри целевого метода
 *          (см. At.target/ordinal/shift) — позволяет вставлять код
 *          в произвольную точку середины метода, не только в начало/конец.
 */
public enum AtType {

    HEAD,
    TAIL,
    INVOKE

}
