package dev.fragmentcode.api.mixin;

/**
 * Указывает, вставлять код ДО или ПОСЛЕ найденной точки (используется
 * вместе с &#64;At(value = AtType.INVOKE, ...)).
 *
 * BEFORE — код выполняется непосредственно перед вызовом target-метода.
 * AFTER  — код выполняется непосредственно после вызова target-метода
 *          (его результат, если есть, уже лежит на стеке/в локальной
 *          переменной — полезно для &#64;ModifyVariable).
 */
public enum Shift {

    BEFORE,
    AFTER

}
